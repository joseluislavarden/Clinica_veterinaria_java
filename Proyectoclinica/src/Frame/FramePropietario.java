    private void JBSELECCIONARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBSELECCIONARActionPerformed
 int fmod = jTable1.getSelectedRow();
       if (fmod >= 0) {
           CARNET.setText(jTable1.getValueAt(fmod, 0).toString());
           NOMBRE.setText(jTable1.getValueAt(fmod, 1).toString());
          APELLIDO.setText(jTable1.getValueAt(fmod, 2).toString());
          CELULAR.setText(jTable1.getValueAt(fmod, 3).toString());
            DIRECCION.setText(jTable1.getValueAt(fmod, 4).toString());
            correo.setText(jTable1.getValueAt(fmod, 5).toString());

        } else {
            JOptionPane.showMessageDialog(this, "No ha seleccionado ");
        }         // TODO add your handling code here:
       Desbloquear();
      JBSELECCIONAR.setEnabled(true);
      eliminarjt.setEnabled(true);
      JBGUARDAR.setEnabled(false);
      nuevo.setEnabled(false);
      MODIFICAR.setEnabled(true);
 
    }//GEN-LAST:event_JBSELECCIONARActionPerformed

